export function normalizeName(name: string) {
    return name.toUpperCase().replace(/ /g, "_"); // Convert to uppercase and replace spaces with underscores
}

export function getCharacterIdByName(name: any) {
    const normalizedInput = normalizeName(name);
    const character = CHARACTERS.find(character => normalizeName(character.name) === normalizedInput);
    return character ? character.id : null; // Return null if not found
}

export function getCharacterNameById(id: number) {
    const character = CHARACTERS.find(character => character.id === id);
    return character ? character.name : null; // Return null if not found
}
export function formatCharacterName(name: string): string {
    return name
        .toLowerCase()
        .split('_')
        .map(word => word.charAt(0).toUpperCase() + word.slice(1))
        .join(' ');
}
